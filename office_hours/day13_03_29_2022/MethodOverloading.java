public class MethodOverloading {
    public static void main(String[] args) {
        /*
        if a CLASS has multiple methods with the same name, it is called OVERLOADING

        ADV :
            -Reusability
            -Flexible
            -Easy to remember

                Bad Practice  :  sum1(int a, int b)  sum2(int a, int b, int c)
                Good practice :  sum(int a, int b)  sum(int a, int b, int c)

        Rules :
                - Number of parameters --- OVERLOADING

                add(int a, int b);
                add(int a);
                add();

                - DataTypes of parameters --- OVERLOADING

                add(int a, float b);

                - Sequence of the parameters -- OVERLOADING

                add(float a, int b);

                - Return Type --- INVALID CASE, THERE IS NO OVERLOADING

                if methOd signatures are same, we can not say there is an OVERLOADING

                METHOD SIGNATURE ---> it includes method name + parameter(s)


         */









    }
}
