package day08_Relational_operators;

public class OperatorPractice2 {
    public static void main(String[] args) {

        int a = 40; // a=40
        System.out.println(--a); // a=39, prints 39
        System.out.println(--a); // a=38, prints 38
        System.out.println(a--); // a=37, prints 38
        System.out.println(--a); // a=36, prints 36
        System.out.println(--a); // a=35, prints 35
        System.out.println(a--); // a=34, prints 35





    }
}
